							<article class="news-post-article content">
								<h2>O que é a pensão alimentícia?</h2>
								<p>A pensão alimentícia é um valor usado para garantir a subsistência de uma pessoa que não pode se manter por conta própria. Dessa forma, a pensão nada mais é do que um pagamento, realizado geralmente todos os meses.
									O valor da pensão alimentícia destina-se ao custeio de despesas básicas do ser humano. Exemplo disso são os gastos com alimentação, saúde, vestimenta, educação, locomoção e lazer.
								</p>

								<h2 class="news-post-title">Quem tem direito?</h2>
								<p>Mas quem tem direito a receber pensão alimentícia? De acordo com o direito brasileiro, podem receber a pensão os filhos, os ex-cônjuges, os ex-companheiros de uma união estável e os pais.
									Assim, podemos dizer que a obrigação de pagar alimentos é recíproca entre os membros de uma família. No entanto, para que uma pessoa possa receber a pensão, deve comprovar que existe a necessidade.
									Com relação aos filhos menores, a pensão alimentícia tem caráter alimentar. Nesse sentido, um dos pais, após a separação ou divórcio, pode ser obrigado a realizar os pagamentos todos os meses. O
									não pagamento pode resultar até mesmo em prisão.
								</p>

								<h2>Como entrar com o pedido de pensão alimentícia?</h2>
								<p> Para entrar com o pedido de pensão alimentícia para filhos menores, o primeiro passo é procurar um advogado da área de família. O interessado deve estar com os documentos da criança, como a certidão de nascimento.
									Outra maneira de pedir a pensão alimentícia é fazendo isso no momento do divórcio ou dissolução da união estável. Se as partes estiverem de acordo, podem combinar o valor e condições. Caso contrário, a
									decisão caberá ao juiz responsável pelo caso, tendo como base os interesses da criança. A pensão alimentícia para o ex-cônjuge ou ex-companheiro também pode ser solicitada no processo de divórcio ou
									dissolução da união estável. Da mesma forma, o pedido pode ser feito em ação separada.
								</p>

								<h2>Até quando pagar pensão alimentícia?</h2>
								<p>O pagamento da pensão alimentícia não é para sempre. Para os filhos menores, frutos de uma relação, a pensão deve ser paga até a maioridade. Ou seja, até que o beneficiário atinja a idade de 18 anos.
									No entanto, se a necessidade permanecer, por exemplo se o filho estiver cursando o pré-vestibular, ensino técnico ou ensino superior e demonstrar a necessidade, o pagamento deve continuar até os 24 anos. Nesse
									caso, o filho deve comprovar que não dispõe de recursos suficientes. Agora, se o ex-cônjuge ou ex-companheiro recebe a pensão, o pagamento é temporário, devendo acontecer até que o beneficiário possa se manter
									por conta própria. Vale a pena destacar que quem está em união estável também tem direito a pensão.
								</p>

								<h2>Como parar de pagar a pensão?</h2>
								<p>Em algumas situações, a pessoa obrigada a pagar a pensão alimentícia pode pedir a exoneração da obrigação. Mas não é possível parar de pagar por livre e espontânea vontade, uma vez que a pensão é um dever previsto
									em lei, é necessário solicitar uma autorização judicial, através de uma ação de exoneração de alimentos. Se o ex-cônjuge que estava recebendo a pensão se casar novamente, aquele que estava pagando pode requerer
									o fim do encargo. Filhos menores, no entanto, continuam recebendo, até que atinjam a idade legal de dezoito anos. E como mencionamos, mesmo os filhos maiores podem receber o valor, se estiverem cursando o
									pré-vestibular, ensino técnico ou superior e forem de baixa renda. Mesmo assim, pode o alimentante – aquele que paga a pensão – solicitar a exoneração, caso entenda que o filho já possui condições de arcar com
									seus gastos, sozinho. A decisão deve ser tomada por um juiz. Vale a pena destacar que deixar de pagar pensão alimentícia pode resultar em prisão civil. O regime é fechado e a condenação pode ser de até três meses. Além disso, os bens do devedor podem ser penhorados, para o pagamento da obrigação.
								</p>

								<h2>Como cobrar as parcelas atrasadas?</h2>
								<p>Existem duas formas de cobrar a pensão alimentícia atrasada. Uma delas é através da penhora de bens, artigo 523 do CPC. Nessa modalidade, a parte interessada deve procurar um advogado, para que exija judicialmente que
									bens do devedor sejam expropriados. O pagamento, nesse caso, pode ser feito através de uma ordem direta ao banco, onde o devedor possua conta, para que o valor seja bloqueado. Além disso, bens móveis e imóveis, como
									casas e carros, podem ser penhorados. Uma das vantagens da penhora dos bens é a possibilidade de cobrar toda a dívida. Sem falar que o juiz pode determinar, ainda, o penhora (desconto) de até 50% do salário do devedor,
									até o pagamento completo das parcelas atrasadas. Outra maneira de cobrar as parcelas atrasadas é através da prisão civil, artigo 528 do CPC. Essa é a única modalidade de dívida que pode gerar cadeia no país. No pedido,
									a parte interessada pode cobrar os últimos três meses vencidos, além das parcelas que vencerem ao longo do processo. Apesar de ser mais drástica, essa medida é uma das que mais funcionam. O devedor pode ser preso por um
									a três meses. Assim que for intimado, o devedor tem o prazo de até três dias para fazer o pagamento ou apresentar o motivo pelo qual não está pagando.
								</p>

								<h2>Como é estipulado o valor da pensão alimentícia?</h2>
								<p>O valor da pensão alimentícia varia de acordo com o caso. Geralmente, os tribunais chegam a um valor usando como base a necessidade de quem este pedindo e as condições de quem vai pagar.
									Além disso, pode ser considerado o padrão de vida que a pessoa mantinha antes da separação ou divórcio. Por isso é comum que pessoas com maior poder aquisitivo sejam obrigadas a pagar mais pensão alimentícia.
									Por exemplo, a pensão alimentícia paga aos filhos menores é calculada sempre em um valor que seja suficiente para cobrir as necessidades básicas. No entanto, não existe uma fórmula específica ou valor percentual 
									fixo para a pensão. Essas são as principais informações a respeito da pensão alimentícia. Esse é um direito previsto em lei, que pode ser exigido entre pais e filhos, ex-companheiros e ex-cônjuges, de forma recíproca.
								</p>
