							<article class="news-post-article content">
								<p>O falecimento de uma pessoa envolve muitas dificuldades, inclusive judiciais. Uma delas é o inventário, um procedimento obrigatório e que precisa ser organizado
									para os sucessores de quem falece e deixa bens. Esse tema pode gerar muitas dúvidas, por haver regras que precisam ser seguidas e variações para cada caso. Isso
									se soma a muitas decisões burocráticas que devem ser tomadas quando alguém morre e à tristeza pela perda. O inventário é responsável pela garantia de que o
									patrimônio do falecido será distribuído de maneira justa e com respeito à sua vontade, afinal, muitas pessoas em vida se preocupam em deixar algum conforto
									financeiro aos seus familiares quando morrerem. Esse é um procedimento importante, que precisa ser conhecido. Para ajudar a esclarecer algumas dúvidas a respeito
									do tema, preparamos um guia completo. Continue a leitura e compreenda melhor.</p>

								<h2 class="news-post-title">O QUE É O INVENTÁRIO?</h2>
								<p>O inventário é um documento que reúne a listagem dos bens que uma pessoa — física ou jurídica — tem. Em caso de falecimento, é preciso registrar essa relação de bens,
									direitos e obrigações, para que seja possível realizar a partilha entre os herdeiros. Assim, o inventário engloba bens de todas as naturezas, como: imóveis, investimentos,
									automóveis, entre outros. Direitos e obrigações também fazem parte desse documento, com exceção daqueles que se destinam exclusivamente ao falecido. Como dito, o inventário
									é obrigatório, portanto, caso não seja feito, a família fica impedida de usufruir dos bens deixados pela pessoa que faleceu. Além disso, se o prazo não for respeitado, é
									preciso arcar com multas. </p>

								<h2>COMO FUNCIONA O PROCESSO DE INVENTÁRIO?</h2>
								<p>O primeiro procedimento a ser feito para dar entrada no inventário é procurar um advogado. É esse profissional que cuidará de todos os passos e garantirá que as determinações
									legais serão cumpridas. A família pode contar com um advogado para atender a todos ou cada um dos herdeiros pode ter o próprio representante. É preciso que seja definido um
									inventariante, geralmente, a pessoa mais próxima do falecido e que responde por todo o grupo de herdeiros. A família precisa fazer o levantamento de todos os bens, das dúvidas
									a respeito do tema e dos dados sobre a partilha. Essa antecipação agiliza o trabalho do advogado, o que faz com que o procedimento seja menos burocrático e tenha menor chance
									de erros. Quando alguma coisa é feita incorretamente, pode haver penalizações, como multas e atrasos. </p>

								<h2>QUAL O PAPEL DO ADVOGADO?</h2>
								<p>Já reforçamos a importância e a obrigatoriedade de se contratar um advogado para cuidar de todo o processo. Nesse sentido, além de ser uma necessidade para a realização do
									inventário, ele pode ser de grande ajuda para a compreensão do procedimento e para garantir a defesa dos interesses da família. Assim, esse profissional auxiliará na obtenção
									dos documentos necessários, mediará os conflitos e defenderá os direitos dos requisitantes. Ele também pode fazer a petição e a elaboração de documentos.</p>

								<h2>QUEM PODE DAR ENTRADA NO PROCESSO DE INVENTÁRIO?</h2>
								<p>A requisição do inventário deve ser feita por qualquer pessoa que tenha a legitimidade para isso. A Lei nº 13.105 de 2015 determina nove pessoas que podem dar a entrada no documento. A preferência,
									nesse caso, é para quem administra os bens do falecido, contudo, não exclui a possibilidade de ser feito por outro indivíduo autorizado. Assim, têm legitimidade para iniciar o procedimento:

								<ul>
									<li> o cônjuge ou companheiro;</li>
									<li> o herdeiro;</li>
									<li> o legatário: a pessoa a quem se deixou a propriedade por vontade;</li>
									<li> o testamenteiro;</li>
									<li> o cessionário: pessoa a quem o herdeiro ou legatário vendeu os direitos;</li>
									<li> o credor do herdeiro, do legatário ou do falecido;</li>
									<li> a Fazenda Pública;</li>
									<li> o administrador judicial da falência de uma das pessoas físicas anteriores.</li>

								</ul>
								</p>

								<h2>QUAIS SÃO OS DOCUMENTOS NECESSÁRIOS PARA DAR ENTRADA NO INVENTÁRIO?</h2>
								<p>Para dar entrada no inventário, é preciso apresentar uma lista de documentos. Eles são diferentes para cada situação, porém, os mais comuns são:

								<ul>
									<li>certidão de óbito;</li>
									<li>procuração de todas as pessoas do grupo;</li>
									<li>testamento ou certidão que comprove a sua inexistência;</li>
									<li>certidão de casamento ou documento que ateste união estável;</li>
									<li>documentos pessoais dos herdeiros.</li>
								</ul>

								Os bens que forem listados no inventário precisam dos documentos correspondentes, como a escritura para imóveis. Além disso, os débitos devem ser registrados com os comprovantes correspondentes.
								</p>

								<h2>QUAIS SÃO AS TAXAS E OS PRAZOS DO INVENTÁRIO?</h2>
								<p>Como se trata de um procedimento obrigatório, existem prazos que devem ser cumpridos. Além disso, o procedimento envolve alguns custos. Essas informações variam de um estado para outro,
									assim como a situação em particular. O prazo para que se dê entrada no inventário é de 60 dias, contados a partir da data do falecimento. Passado esse período, a Fazenda Pública deverá
									cobrar multa, com juros e correção monetária. Com relação às taxas, é preciso pagar os impostos obrigatórios e os honorários do advogado. No caso de procedimento judicial, são somadas
									as despesas processuais. Quando isso é feito extrajudicialmente, os custos devem ser com o cartório. É válido destacar que, quando ocorrem divergências familiares, o valor a ser pago
									tende a aumentar, já que precisam ser feitos procedimentos extras, além de os honorários do advogado se elevarem. Dentre os impostos, o mais importante é o ITCMD. Conheça melhor a seguir.
								</p>

								<h2>COMO FUNCIONA O ITCMD?</h2>
								<p>O Imposto de Transmissão de Causa Mortis e Doação (ITCMD), também conhecido como “imposto sobre a herança”, é um tributo estadual que envolve heranças, transmissões de bens, doações, entre
									outros procedimentos em que um bem é repassado para outra pessoa sem cobrança. Para saber qual o valor percentual da taxa cobrada em seu estado, você pode consultar a Secretaria da Fazenda
									ou um cartório. Esse imposto é obrigatório em todos os tipos de inventários e é cobrado da parte recebida por cada herdeiro, ou seja, é proporcional ao valor herdado.
								</p>

								<h2>O QUE ACONTECE EM CASOS DE DÍVIDA?</h2>
								<p>Conforme apresentamos, ao fazer o inventário, são listados as dívidas e os bens. Assim, antes de o valor ser distribuído entre os familiares, todos os débitos que não se anulam com o falecimento
									são quitados com o valor do patrimônio do falecido. Existem casos em que o saldo devedor pode ser maior do que o dos bens. Nessa circunstância, é possível solicitar o inventário negativo já mencionado,
									que comprova que o valor deixado é insuficiente para o pagamento. Entretanto, também é possível que se herde a dívida de uma pessoa falecida, por exemplo, as parcelas em aberto de um imóvel ou de um
									automóvel. Essa responsabilidade é dividida entre a família.
								</p>

								<h2>QUAL A IMPORTÂNCIA DE TOMAR DECISÕES AINDA EM VIDA?</h2>
								<p>Apesar de o inventário ser um documento feito após o falecimento de uma pessoa, é possível assumir grande parte das decisões em vida e evitar muitos transtornos para os familiares. Isso se chama planejamento
									 sucessório ou “inventário em vida”. Pensar a respeito desse tema com antecedência pode parecer estranho, mas é um gesto de cuidado tornar mais simples o acesso aos bens, afinal, eles podem fazer muita diferença 
									 para os familiares. Além disso, um planejamento antecipado reduz grande parte dos gastos, o que garante um patrimônio maior para a família. Veja alguns dos pontos importantes de se ter um cuidado preventivo.
								</p>

